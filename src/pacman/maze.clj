(defn characterController
  [char]
  (cond
    (= char \·) (conj [] char {:fg :yellow})
    (= char \●) (conj [] char {:fg :white})
    (or (= char \#) (= char \<) (= char \^)
        (= char \*) (= char \x) (= char \X)) (conj [] \space)
    :else (conj [] char {:fg :blue})
    )
  )

(defn mazeSequenceHandler
  [maze_data]
  (loop [mz_dt maze_data
         result []]
    (if (empty? mz_dt)
      result
    (recur (rest mz_dt)
           (conj result (mapv characterController (first mz_dt))))))
  )

(defn getCharData
  [maze_pt]
  (loop [str-seq maze_pt
         result []]
    (if (empty? str-seq)
      result
      (recur (rest str-seq)
             (conj result (first str-seq))))
    )
  )

(defn getMazeData
  [maze]
  (loop [seq-iter maze
         result []]
    (if (empty? seq-iter)
      result
      (recur (rest seq-iter) (conj result (getCharData (seq (first seq-iter))))))
  ))

(defn buildMazeData
  [n]
  (if (or (> n 12) (< n 0))
    (throw (Exception. "Out of bounds maze access"))
    (getMazeData (mazes n))))


(defn buildScatterMazeData
  [n]
  (if (or (> n 12) (< n 0))
    (throw (Exception. "Out of bounds maze access"))
    (getMazeData (scatter_mazes n))))
